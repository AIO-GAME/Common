namespace AIO
{
    /// <summary>
    /// 枚举类型，表示一些固定的角度值。
    /// </summary>
    public enum EAngleFixed
    {
        /// <summary>
        /// 角度为180度。
        /// </summary>
        DegreesA180 = 180,

        /// <summary>
        /// 角度为135度。
        /// </summary>
        DegreesA135 = 135,

        /// <summary>
        /// 角度为90度。
        /// </summary>
        DegreesA90 = 90,

        /// <summary>
        /// 角度为45度。
        /// </summary>
        DegreesA45 = 45,

        /// <summary>
        /// 角度为0度。
        /// </summary>
        Zero = 0,

        /// <summary>
        /// 角度为-45度。
        /// </summary>
        DegreesS45 = -45,

        /// <summary>
        /// 角度为-90度。
        /// </summary>
        DegreesS90 = -90,

        /// <summary>
        /// 角度为-135度。
        /// </summary>
        DegreesS135 = -135,

        /// <summary>
        /// 角度为-180度。
        /// </summary>
        DegreesS180 = -180,
    }
}

namespace AIO
{
    /// <summary>
    /// 一元运算符
    /// </summary>
    public enum UnaryOperator
    {
        /// <summary>
        /// 逻辑非
        /// </summary>
        LogicalNegation,

        /// <summary>
        /// 取反
        /// </summary>
        NumericNegation,

        /// <summary>
        /// 自增 ++
        /// </summary>
        Increment,

        /// <summary>
        /// 自减 --
        /// </summary>
        Decrement,

        /// <summary>
        /// 加
        /// </summary>
        Plus,
    }
}
